<script setup lang="ts">
  import { onMounted, onUnmounted, reactive, ref } from "vue";

  const loaded = ref(false);
  const statsRef = ref(null);
  const statistics = reactive([
    { label: "çº¹æ ·é‡‡é›†", value: 500, current: 0, suffix: "+" },
    { label: "åˆä½œé«˜æ ¡", value: 10, current: 0, suffix: "+" },
    { label: "åˆ›æ–°å¯èƒ½", value: "âˆ", current: 0, suffix: "", isSymbol: true },
  ]);

  // è§†å·®æ•ˆæœçŠ¶æ€
  const parallaxStyle = ref<{ transform: string; transition?: string }>({
    transform: "translate(0, 0)",
  });

  onMounted(() => {
    // 1. é¦–å±åŠ¨ç”»
    setTimeout(() => {
      loaded.value = true;
    }, 100);

    // 2. æ•°å­—å¢é•¿åŠ¨ç”»ç›‘å¬
    const observer = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            startCounting();
            observer.unobserve(entry.target);
          }
        });
      },
      { threshold: 0.5 }
    );

    if (statsRef.value) {
      observer.observe(statsRef.value);
    }
  });

  // æ•°å­—å¢é•¿é€»è¾‘
  const startCounting = () => {
    statistics.forEach((stat) => {
      if (stat.isSymbol) return;

      // ç®€å•çš„ç¼“åŠ¨å¢é•¿
      const duration = 2000; // 2ç§’
      const start = 0;
      const end = stat.value;
      const startTime = performance.now();

      const animate = (currentTime: number) => {
        const elapsed = currentTime - startTime;
        const progress = Math.min(elapsed / duration, 1);

        // easeOutQuart ç¼“åŠ¨å‡½æ•°
        const ease = 1 - (1 - progress) ** 4;

        stat.current = start + (end - start) * ease;

        if (progress < 1) {
          requestAnimationFrame(animate);
        }
      };

      requestAnimationFrame(animate);
    });
  };

  const scrollToContent = () => {
    const el = document.getElementById("mission");
    if (el) {
      el.scrollIntoView({ behavior: "smooth" });
    }
  };

  // ç®€å•çš„é¼ æ ‡è§†å·®é€»è¾‘
  const handleImageParallax = (e: MouseEvent) => {
    const { clientX, clientY } = e;
    // è®¡ç®—ç›¸å¯¹äºçª—å£ä¸­å¿ƒçš„åç§»
    const x = (clientX - window.innerWidth / 2) / 30; // ç§»åŠ¨å¹…åº¦å› å­
    const y = (clientY - window.innerHeight / 2) / 30;

    parallaxStyle.value = {
      transform: `perspective(1000px) rotateY(${x}deg) rotateX(${-y}deg) translateZ(10px)`,
    };
  };

  const resetParallax = () => {
    parallaxStyle.value = {
      transform: "perspective(1000px) rotateY(0) rotateX(0) translateZ(0)",
      transition: "transform 0.5s ease",
    };
  };
</script>

